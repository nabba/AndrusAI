"""
Phase 3: SubIA integrity manifest + adversarial Tier-3 tampering tests.

Two layers verify:

  1. The canonical in-repo manifest at app/subia/.integrity_manifest.json
     matches the live `app/subia/` tree. If this fails, the deploy has
     diverged from committed source (or the manifest is stale and
     should be regenerated by running `python -m app.subia.integrity`
     or the equivalent).

  2. Adversarial: a simulated Self-Improver mutation on a Tier-3 file
     is detected by verify_integrity as a hash mismatch, and rejected
     by SafetyGuardian.enforce_tier_boundaries. This is the
     CLAUDE.md invariant expressed as a test: "the Self-Improver
     cannot modify its own evaluation criteria".

Together they demonstrate that both deploy-time and runtime tampering
are caught.
"""

